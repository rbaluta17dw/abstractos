package Ciudad;

public class CuentaBancaria {
	private String numero;
	private String saldo;

	
	public String getNumero() {
		return numero;
	}

	public void setNumero(String cuenta) {
		this.numero = cuenta;
	}

	public String getSaldo() {
		return saldo;
	}

	public void setSaldo(String saldo) {
		this.saldo = saldo;
	}

}
